/**
 * 여러줄 주석처리 하는 방법
 */
// 한줄짜리 주석

public class Helloworld { // Helloworld 라는 클래스 정의  public 접근제어자
    public  static void main(String[] args) {  // main이라는 method가 실행됨 main안에 문자열 파라미터를 받음
        System.out.println("Hello world!");   // static 뒤에 void 메소드 리턴 값의 자료형 (void일 경우 리턴 값이 없음)
    }                                         // System이라는 클래스의 out이라는 변수의 println이라는 메소드 호출
}
